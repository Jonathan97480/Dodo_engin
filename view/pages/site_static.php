                <h3>Qu'est-ce qu'un site statique?</h3>
                <div>
                    <img src="<?= Router::url('img/icon/img_static.jpg') ?>" alt="">
                </div>

                <br>
                <p> On entend par page statique, non pas une page sans mouvements ou sans animations, mais une page visible telle qu'elle a été conçue.</p>

                <p> Ces pages peuvent présenter toute forme de contenu, animations flash, images, musique, vidéo etc... mais elles sont toujours présentées de la même façon. 
                    Elles ne changent pas et c'est en ce sens qu'elles sont statiques.</p>

                <p> Qu'est-ce qu'un site dynamique?
                    En opposition aux pages statiques, les pages dynamiques permettent de présenter les informations de différentes manières selon l'interaction avec le visiteur.
                    Les pages sont alors construites "à la volée" grâce à une programmation conçue par le webmaster.</p>

                <p> Le contenu est issu d'une base de données en fonction de critères établis par l'internaute puis mis en page en temps réel.
                    C'est le cas par exemple d'un site e-commerce: présentation des articles par thèmes, couleurs, prix etc...
                    C'est également le cas des blogs et des forums où les visiteurs peuvent participer au contenu du site.
                    C'est aussi le cas d'un système de mises à jour.</p>

                <p> Comment choisir si une page est statique ou dynamique?
                    Tout d'abord il convient de déterminer le rôle de la page.
                    Tout système d'interaction avec le visiteur nécessitera une partie dynamique.
                    Pour un système de mise à jour, il conviendra de réfléchir aux fréquences des mises à jour. Si le contenu ne change peu ou qu'une fois à l'année, 
                    il n'est pas nécessaire de passer par des pages dynamiques. Une intervention ponctuelle du webmaster peut très bien convenir.</p>

                <p> Quels sont les avantages et les inconvénients des deux méthodes?
                    L'utilisation des pages dynamiques est plus souple pour l'évolution du site mais nécessite plus de temps de travail pour la programmation.
                    En opposition, les pages statiques sont plus "figées" mais plus simples à réaliser.</p>

