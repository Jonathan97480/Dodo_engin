                <h3> Site dynamique ou site statique, quelles différences ?</h3>
                <p> Dans le jargon, nous parlons souvent de site dynamique ou de site statique. Evidemment, l’objectif n’est pas de vous embrouiller,
                    mais cela finit souvent par arriver. Il s’agit en réalité de deux modes de conception complètement opposés. La plupart du temps,
                    c’est le site dynamique qui est retenu, pour sa praticité d’usage, ainsi que sa souplesse. Bonne nouvelle, WordPress est une solution
                    permettant la création de site internet dynamique.</p>

                <h3> Un site internet statique</h3>
                <p> Un site web statique possède des avantages certains : une rapidité d’ouverture bien supérieure à un site dynamique, un meilleur contrôle technique, et c’est à peu près tout. Son usage est particulièrement adapté à un mini site vitrine.</p>

                <p> Par contre, ses désavantages sans être très nombreux sont de taille.
                <p>

                <p> <strong> Désavantage principal d’un site statique : la modification de contenu</strong>. Ben quoi la modification de contenu ? Sachez qu’avec un site internet statique, si vous décidez subitement de changer
                    un lien dans un menu, ou une virgule dans votre slogan, vous pouvez. Cependant pour ce faire, vous devrez vous connecter à votre serveur FTP, télécharger les fichiers contenant l’information à modifier.
                    Vous devrez ensuite effectuer vos changements et renvoyer tout ça sur le serveur. Imaginez la perte de temps sur 200 pages, et surtout voyez le risque d’erreur…</p>

                <p> D’autre part, chaque modification présente un risque certain, car vu que vous devrez éditer le code de votre site internet, tout se joue à la virgule près. Ne vous y aventurez pas sans savoir ce que vous faites.</p>

                <p> Si vous vous lancez dans l’aventure d’une création site web statique vous même, savoir coder sera un pré-requis. Des connaissances techniques vous seront nécessaires comme le langage html css.</p>

                <h3> Un site internet dynamique</h3>
                <p> Un site dynamique contrairement à un site statique est complètement autonome. Il fonctionne (la plupart du temps) en interconnexion avec une base de données (MYSQL).
                    Cette base lui permet de stocker un grand nombre d’informations de façon hiérarchisée et sécurisée. Quand un visiteur se rend sur votre site dynamique, le serveur interroge votre CMS
                    (dans notre cas WordPress), pioche le contenu opportun dans la base de données et l’affiche dans le design prévu. C’est ce que l’on appelle un système de gestion de contenu.</p>

                <p> Un bon site web dynamique possède également un backoffice, qui permet d’effectuer des modifications rapidement et simplement. Depuis ce backoffice, vous pourrez également optimiser
                    vos articles pour le référencement naturel, mais aussi rajouter des images, mettre en forme votre texte, consulter vos statistiques en un clic.</p>

                <p> Pour parler plus en détail du backoffice de WordPress, sachez que chaque modification s’effectue comme un jeu d’enfant avec son interface intuitive. Lors de la modification d’un article,
                    aucune connaissance technique ne vous sera obligatoire (même si dans certains cas, ça peut dépanner). L’éditeur, dont vous avez une photo ci-dessous est ce que l’on appelle WYSIWYG,
                    what-you-see-is-what-you-get en anglais. En français : Ce que vous voyez est ce que vous avez.</p>

                <p> Habituellement, pour créer une boutique en ligne, voire pour créer un site professionnel, il est bien mieux de se diriger vers un site dynamique. Un site web doit être conçu pour la polyvalence, et doit être agréable à utiliser au quotidien. C’est le cas de WordPress.</p>

                <h3> Pourquoi il n’y a pas que des sites dynamiques ?</h3>
                <p> Ce n’est pas si simple. Effectivement, un site dynamique est bardé d’options, et il nécessite une foule d’optimisations. Par souci d’économie,
                    ou de gain de temps, certains préfèrent s’orienter vers des sites statiques. Le choix d’un site statique peut être judicieux si vous exposez tout votre contenu
                    en un minimum de pages. Il faut savoir que si votre site comporte plus de 10 pages, vous devriez songer très fortement à passer sur un système dynamique.</p>

                <p> Egalement, même si les modifications éditoriales sont aisées avec un site dynamique, il faut être prudent avec les modifications techniques. Ces dernières si elles sont mal réalisées
                    peuvent nuire au bon fonctionnement de votre site internet, ou pire, à son bon référencement.</p>

                <p> C’est pour cette raison que tous mes clients reçoivent personnellement une formation offerte avec la création de leur site internet, afin d’apprendre à l’utiliser convenablement.
                    J’explique justement ce qui peut être fait sans crainte, et ce qui doit être fait par un professionnel. Il n’y a donc pas de mauvaises surprises.</p>

                <h3> Et les plateformes “toutes prêtes” alors ?</h3>
                <p> Les plateformes comme Wix, Jimdo ou encore Weebly vous proposeront pour un prix mensuel relativement élevé des sites considérés comme dynamiques. Leur gros avantage est leur côté intuitif, mais ils ne sont pas vraiment efficaces en terme d’originalité ou de référencement web…</p>

